function var=ODE_LHS(t,y,pars)
%% PARAMETERS %%
% parameter;
FSH	=	y	(	1	)	;
LH	=	y	(	2	)	;
FSHp	=	y	(	3	)	;
LHp	=	y	(	4	)	;
phi	=	y	(	5	)	;
omega	=	y	(	6	)	;
lamda	=	y	(	7	)	;
S	=	y	(	8	)	;
Ty	=	y	(	9	)	;
T	=	y	(	10	)	;
E2	=	y	(	11	)	;
P4	=	y	(	12	)	;

CFE	=pars(	1	);
CFI	=pars(	2	);
CFP	=pars(	3	);
CLE	=pars(	4	);
CLP	=pars(	5	);
CLT	=pars(	6	);
deltaF	=pars(	7	);
deltaL	=pars(	8	);
kF	=pars(	9	);
KFI	=pars(	10	);
KiLP	=pars(	11	);
KLT	=pars(	12	);
KmL	=pars(	13	);
kL	=pars(	14	);
n	=pars(	15	);
V	=pars(	16	);
v0L	=pars(	17	);
v1L	=pars(	18	);
vF	=pars(	19	);
cphiF	=pars(	20	);
deltas	=pars(	21	);
f0	=pars(	22	);
f1	=pars(	23	);
f2	=pars(	24	);
h1	=pars(	25	);
h2	=pars(	26	);
hp	=pars(	27	);
hs	=pars(	28	);
l	=pars(	29	);
m	=pars(	30	);
s	=pars(	31	);
w	=pars(	32	);
CTF2	=pars(	33	);
deltaE	=pars(	34	);
deltaP	=pars(	35	);
deltaT	=pars(	36	);
e0	=pars(	37	);
eta	=pars(	38	);
h3	=pars(	39	);
k1	=pars(	40	);
k2	=pars(	41	);
k3	=pars(	42	);
p	=pars(	43	);
t0	=pars(	44	);
t1	=pars(	45	);
t2	=pars(	46	);
tao1	=pars(	47	);
tao2	=0;
tao3	=pars(	48	);
tg1	=pars(	49	);
tg2	=pars(	50	);
psi	=pars(	51	);
G1=1;
G2=1;

dFSHp=vF/(1+CFI*S*lamda/(KFI+S*lamda))-kF*(1+CFP*P4)/(1+CFE*E2^2)*FSHp;
dFSH=1/V*kF*(1+CFP*P4)/(1+CFE*E2^2)*FSHp-deltaF*FSH;
dLHp=(v0L*T/(KLT+T)+v1L*(E2.^n)/(KmL.^n+E2.^n))/(1+P4/(KiLP*(1+CLT*T)))-kL*(1+CLP*P4)/(1+CLE*E2)*LHp;
dLH=1/V*kL*(1+CLP*P4)/(1+CLE*E2)*LHp-deltaL*LH;
dphi=f0*T/273.67+(f1*FSH^2/((h1/(1+0.19878*T/273.67))^2+FSH^2)-f2*LH^2/((h2/(1+cphiF*FSH))^2+LH^2))*phi;
domega=(f2*LH^2/((h2/(1+cphiF*FSH))^2+LH^2))*phi-w*S*omega;
dlamda=w*S*omega-l*(1-S)*lamda;
dS=s*(LH.^m)/((hs.^m)+(LH.^m))*(1-S)-deltas*S;
D=k1*LH^2+k2*LH+k3;
F1=(LH^2)/D;
F2=LH/D;
dT=t0-deltaT*T+(t1*G1*(F1+CTF2*F2)+t2*G1*G2*F1)*(phi+tao1*omega+tao2*S*lamda+tao3*(1-(phi+omega+lamda)/psi));
dTy=tg1*G1*G2*F1-tg2*FSH/(h3+FSH)*Ty;
dE2=e0-deltaE*E2+tg2*FSH/(h3+FSH)*Ty*(phi+eta*lamda*S);
dP4=-deltaP*P4+p*LH/(LH+hp)*lamda*S;
var=[dFSH; dLH; dFSHp; dLHp; dphi; domega; dlamda; dS; dTy; dT; dE2; dP4];